a=1
print(type(a),"\t\t",a)                     #Datatype integer

b=3.14
print(type(b),"\t",b)                       #Datatype float


c="Abbas"
print(type(c),"\t\t",c)                     #Datatype string


d=True
print(type(d),"\t\t",d)                     # Boolean '1' is true '0' is false

e=[1,2,3,4]
print(type(e),"\t\t",e)                     #Datatype List, Stores multiple items in a Variable. (Mutable)

f=(5,4,3,2,1)
print(type(f),"\t",f)                     #Datatype Tuple, Stores multiple items, (IMMUTABLE)

g={1,3,5,7}
print(type(g),"\t\t",g)                     #Datatype Set, Stores multiple items, Unordered, No Indexing, No repeat items
